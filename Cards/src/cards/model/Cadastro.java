    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //m√©todo para adicionar registros em uma tabela
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://127.0.0.1/icards","root","mysqlmysql");
            String query = "insert into USUARIO (nome, cpf, tipo, email, senha, endereco, telefone, data, nacionalidade) values (?,?,?,?,?,?,?,?,?)";
            
            //para cada atributo da tabela ser√° associado o jText correspondente por ordem
            
            PreparedStatement stmt = con.prepareStatement(query);
            
           
            stmt.setString(1, jTextField1.getText()); //Nome
            stmt.setString(2, jTextField2.getText());//cpf
            stmt.setString(3, jTextField3.getText());//tipo
            stmt.setString(4, jTextField4.getText());//email
            stmt.setString(5, jTextField5.getText());//senha
            stmt.setString(6, jTextField6.getText());//endereco
            stmt.setString(7, jTextField7.getText());//telefone
            stmt.setString(8, jTextField8.getText());//data
            stmt.setString(9, jTextField9.getText());//nacionalidade
            
            
            stmt.executeUpdate();
            
            stmt.close();
            con.close();
              
        }catch (ClassNotFoundException ex) {
         System.out.println("Classe n√£o encontrada "+ex.getMessage());
        } catch (SQLException ex) {
            System.out.println("Erro de SQL "+ ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

